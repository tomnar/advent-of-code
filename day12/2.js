// Use cache to reuse calculations, since we are spawning a lot of configs with similar conditions (in case of ?)
const cache = new Map();
function calcConfig(condition, groups, currentGroup = 0) {
  const key = JSON.stringify([condition, groups, currentGroup]);
  if (cache.has(key)) {
    return cache.get(key);
  }

  // base cases
  if (condition.length === 0) {
    if (currentGroup) {
      cache.set(key, Number(groups.length === 1 && currentGroup === groups[0]));
      return Number(groups.length === 1 && currentGroup === groups[0]);
    } else {
      cache.set(key, Number(groups.length === 0));
      return Number(groups.length === 0);
    }
  }

  if (currentGroup) {
    // current group is too long
    if (!groups.length || currentGroup > groups[0]) {
      cache.set(key, 0);
      return 0;
    }
  }

  // recursion
  if (condition[0] === ".") {
    // if a group has started, check if it has the correct length
    // -> true: move onto the next group
    // -> false: terminate
    // If a group hasn't started, do nothing
    if (currentGroup) {
      if (currentGroup !== groups[0]) {
        cache.set(key, 0);
        return 0;
      } else {
        groups = groups.slice(1);
      }
    }
    const result = calcConfig(condition.slice(1), groups, 0);
    cache.set(key, result);
    return result;
  }
  if (condition[0] === "#") {
    // increment current group length
    const result = calcConfig(condition.slice(1), groups, currentGroup + 1);
    cache.set(key, result);
    return result;
  } else {
    // condition === ?
    if (!groups.length || currentGroup === groups[0]) {
      // if current group length matches the first group in groups
      // or that there are no remaining groups
      // then the current location must be #
      const result = calcConfig(condition.slice(1), groups.slice(1), 0);
      cache.set(key, result);
      return result;
    } else {
      // if current group length doesn't match the first group in groups
      // and the group has started, then the current location must be .
      if (currentGroup) {
        const result = calcConfig(condition.slice(1), groups, currentGroup + 1);
        cache.set(key, result);
        return result;
      } else {
        // if the group haven't started, then it doesn't have to start now
        const result =
          calcConfig(condition.slice(1), groups, currentGroup + 1) +
          calcConfig(condition.slice(1), groups, currentGroup);
        cache.set(key, result);
        return result;
      }
    }
  }
}

